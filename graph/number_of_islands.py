def numIslands(grid):

    if not grid:
        return 
    
    nrows = len(grid)
    ncols = len(grid[0])
    island_count = 0

    def dfs(i, j):
        if i < 0 or j < 0 or i >= nrows or j >= ncols or grid[i][j] == 0:
            return

        
        grid[i][j] = 0

        dfs(i - 1, j)
        dfs(i + 1, j)
        dfs(i, j - 1)
        dfs(i, j + 1)



    for i in range(nrows):
        for j in range(ncols):
            if grid[i][j] == 1:
                island_count += 1
                dfs(i, j)

    return island_count


